def english_to_morse(
    input_file: str = "lorem.txt", output_file: str = "lorem_morse.txt"
):
    """Convert an input text file to an output Morse code file.

    Notes
    -----
    This function assumes the existence of a MORSE_CODE dictionary, containing a
    mapping between English letters and their corresponding Morse code.

    Parameters
    ----------
    input_file : str
        Path to file containing the text file to convert.
    output_file : str
        Name of output file containing the translated Morse code. Please don't change
        it since it's also hard-coded in the tests file.
    """

    # We store the final results here
    translated_content = []

    with open(input_file, "r") as file1:
        # We read each line from the source
        for line in file1:
            line_result = []
            for char in line.upper():
                if char in MORSE_CODE:
                    line_result.append(MORSE_CODE[char])
                elif char == " " or char == "\n":
                    # Per your requirement: spaces go to a new line
                    line_result.append("\n")

            # Join characters with a space for clarity, then add to our list
            translated_content.append("".join(line_result))

    # Join everything into one big string to write
    final_output = "".join(translated_content)

    with open(output_file, "w") as file2:
        file2.write(final_output)

    return final_output


